public class Variables {
    
    public static void main(String[] args){
        
        //Tipos de datos de numeros enteros: byte, short, int, long
        
        byte num = 18; // representa +- 128
         
        short num2 = 34; // representa +- 32768
         
        int num3 = 12;  // representa +- 2.147.483.648
        
        long num4 = 4321;  // representa demasiados.
        
        System.out.println(num);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
                
        
        // Luego estan los decimales como float y double
        
    }
}
